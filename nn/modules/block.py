class HyperComputeModule(nn.Module):
    def __init__(self, c1, c2, threshold):
        threshold=0.5
        super().__init__()
        self.threshold = threshold
        self.hgconv = HyPConv(c1, c2)
        self.bn = nn.BatchNorm2d(c2)
        self.act = nn.SiLU()

    def forward(self, x):
        b, c, h, w = x.shape  # 获取输入张量的形状
        x = x.view(b, c, -1).transpose(1, 2).contiguous()  # (b, h*w, c)
        feature = x.clone()

        # 使用余弦相似度计算特征间的相似性
        norm_feature = F.normalize(feature, p=2, dim=-1)  # 特征归一化
        similarity = torch.matmul(norm_feature, norm_feature.transpose(1, 2))  # 点积

        # 根据阈值构建邻接矩阵
        hg = similarity > self.threshold  # 将相似度大于阈值的位置设为 1，其余为 0
        hg = hg.float().to(x.device).to(x.dtype)

        # 应用超图卷积
        x = self.hgconv(x, hg).to(x.device).to(x.dtype) + x

        # 恢复原始形状
        x = x.transpose(1, 2).contiguous().view(b, c, h, w)
        x = self.act(self.bn(x))  # 批归一化 + 激活函数

        return x

    

